package com.basicday1;

public class IfElseStatements {
	public static void main(String[]args) {
		int a=6;
		int b=10;

		if(a==b)
		{
			System.out.println("a is equal to b");
		}
		else if(a>b)
		{
			System.out.println("a is greater than b");
		}
		else 
		{
			System.out.println("a is less than b");
		}

	}

}
